#include "libft.h"

int	ft_atoi(const char *nptr)
{
	short			i;
	short			minus;
	long long int	ans;

	i = 0;
	minus = 1;
	ans = 0;
	while (nptr[i] == ' ' || nptr[i] == '\t' || nptr[i] == '\n'
		|| nptr[i] == '\v' || nptr[i] == '\f' || nptr[i] == '\r')
		i++;
	if (nptr[i] == '-')
		minus = -1;
	if (nptr[i] == '-' || nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
		ans = ans * 10 + (nptr[i++] - '0');
	return ((int)ans * minus);
}
